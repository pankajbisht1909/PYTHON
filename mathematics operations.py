#TO FIND THE MEAN
import statistics
data=[2,4,20,8,10]
print("The mean of the data is:",statistics.mean(data))

#TO FIND THE MEDIAN
print("The median of the data is:",statistics.median(data))

#TO FIND THE VARIANCE
print("The median of the data is:",statistics.variance(data))

#TO FIND THE STANDARD DEVIATION
print("The median of the data is:",statistics.stdev(data))


